class Funcionario (Pessoa, Usuario):
    def __init__ (self, id, nome, acesso, funcao):
        super().__init__(id, nome, endereco)
        super().__init__(user, acesso, senha)
        self._funcao = funcao

def getFuncao(self):
    return self._funcao

def setFuncao(self, funcao):
    self._funcao = funcao

